export function getThemePalette(theme: TerminalTheme): TerminalPalette {
  if (theme?.preset === 'custom' && 'palette' in theme) return theme.palette;
  const preset = THEME_PRESETS.get(theme?.preset || 'dark') ?? THEME_PRESETS.get('dark');
  if (!preset) throw new Error('Default preset theme not found');
  return preset;
}
