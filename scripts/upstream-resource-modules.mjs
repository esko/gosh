#!/usr/bin/env node
/**
 * Generate the small libdot/hterm resource modules normally emitted into the
 * upstream submodule's ignored dist/ directories by Rollup.
 *
 * Gosh serves the upstream source module graph directly, so keep equivalent
 * tracked modules beside that source graph instead of depending on ignored
 * submodule build output.
 */

import { spawnSync } from 'node:child_process';
import fsp from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const LICENSE_HEADER = `// Copyright 2023 The ChromiumOS Authors
// Use of this source code is governed by a BSD-style license that can be
// found in the LICENSE file.

// Generated by scripts/upstream-resource-modules.mjs from upstream/libapps.
// Do not edit this file directly.

`;

async function readJson(file) {
  return JSON.parse(await fsp.readFile(file, 'utf8'));
}

async function readText(root, relative) {
  return fsp.readFile(path.join(root, relative), 'utf8');
}

async function dataUrl(root, relative, mime) {
  const data = await fsp.readFile(path.join(root, relative));
  return `data:${mime};base64,${data.toString('base64')}`;
}

function gitValue(root, args, label) {
  const result = spawnSync('git', ['-C', root, ...args], { encoding: 'utf8' });
  if (result.status !== 0) {
    throw new Error(`unable to read upstream ${label}: ${(result.stderr || '').trim()}`);
  }
  return result.stdout.trim();
}

async function rewriteEntryImport(file, generatedImport, ignoredDistImport) {
  let source = await fsp.readFile(file, 'utf8');
  if (source.includes(ignoredDistImport)) {
    source = source.replace(ignoredDistImport, generatedImport);
    source = source.replace(
      /@suppress \{moduleLoad\} Don't try and load dist\/js\/[^.]+\.js\./,
      '@suppress {moduleLoad} Resource module is generated by Gosh asset sync.',
    );
    await fsp.writeFile(file, source, 'utf8');
  } else if (!source.includes(generatedImport)) {
    throw new Error(`${file} does not contain the expected resource import`);
  }
}

export async function generateUpstreamResourceModules({ libappsRoot, outDir }) {
  const libdotRoot = path.join(libappsRoot, 'libdot');
  const htermRoot = path.join(libappsRoot, 'hterm');
  const libdotPackage = await readJson(path.join(libdotRoot, 'package.json'));
  const htermPackage = await readJson(path.join(htermRoot, 'package.json'));
  const gitCommit = gitValue(libappsRoot, ['rev-parse', 'HEAD'], 'commit');
  const gitDate = gitValue(libappsRoot, ['log', '-1', '--format=%cI'], 'date');

  const libdotModule = `${LICENSE_HEADER}export const gitDate = ${JSON.stringify(gitDate)};
export const version = ${JSON.stringify(libdotPackage.version)};
`;
  const htermResources = {
    AU_BELL: await dataUrl(htermRoot, 'audio/bell.ogg', 'audio/ogg'),
    GIT_COMMIT: gitCommit,
    GIT_DATE: gitDate,
    HTML_FIND_BAR: await readText(htermRoot, 'html/find_bar.html'),
    HTML_FIND_SCREEN: await readText(htermRoot, 'html/find_screen.html'),
    IMG_CLOSE: await readText(htermRoot, 'images/close.svg'),
    IMG_COPY: await readText(htermRoot, 'images/copy.svg'),
    IMG_KEYBOARD_ARROW_DOWN: await readText(htermRoot, 'images/keyboard_arrow_down.svg'),
    IMG_KEYBOARD_ARROW_UP: await readText(htermRoot, 'images/keyboard_arrow_up.svg'),
    IMG_ICON_96: await dataUrl(htermRoot, 'images/icon-96.png', 'image/png'),
    VERSION: htermPackage.version,
  };
  const htermModule = LICENSE_HEADER + Object.entries(htermResources)
    .map(([name, value]) => `export const ${name} = ${JSON.stringify(value)};`)
    .join('\n') + '\n';

  const generated = [
    ['libdot/js/libdot_resources.js', libdotModule],
    ['hterm/js/hterm_resources.js', htermModule],
  ];
  for (const [relative, source] of generated) {
    const output = path.join(outDir, relative);
    await fsp.mkdir(path.dirname(output), { recursive: true });
    await fsp.writeFile(output, source, 'utf8');
  }
  await rewriteEntryImport(
    path.join(outDir, 'libdot/index.js'),
    "'./js/libdot_resources.js'",
    "'./dist/js/libdot_resources.js'",
  );
  await rewriteEntryImport(
    path.join(outDir, 'hterm/index.js'),
    "'./js/hterm_resources.js'",
    "'./dist/js/hterm_resources.js'",
  );
  return generated.map(([dest, source]) => ({ dest, bytes: Buffer.byteLength(source), source: '(generated from upstream sources)' }));
}

if (process.argv[1] === fileURLToPath(import.meta.url)) {
  const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
  await generateUpstreamResourceModules({
    libappsRoot: path.join(repoRoot, 'upstream/libapps'),
    outDir: path.join(repoRoot, 'app/upstream'),
  });
}
